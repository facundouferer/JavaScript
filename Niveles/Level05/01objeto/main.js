// Definir un objeto literal
var miObjeto = {
    propiedad1: "valor1",
    propiedad2: "valor2",
    propiedad3: "valor3"
};

// Acceder a las propiedades del objeto
console.log(miObjeto.propiedad1);
console.log(miObjeto.propiedad2);
console.log(miObjeto.propiedad3);

//también podemos acceder a los valores con la notación corchetes []

console.log(miObjeto["propiedad1"]);
console.log(miObjeto["propiedad2"]);
console.log(miObjeto["propiedad3"]);
